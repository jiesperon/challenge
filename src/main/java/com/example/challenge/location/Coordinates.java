package com.example.challenge.location;

public interface Coordinates {
	
	/**
	 * Get Latitude
	 * 
	 * @return Double
	 */
	public Double getLatitude();
	
	/**
	 * Set Latitude
	 * 
	 * @param latitude
	 */
	public void setLatitude(Double latitude);
	
	/**
	 * Get Longitude
	 * 
	 * @return Double
	 */
	public Double getLongitude();
	
	
	/**
	 * Set Longitude
	 * 
	 * @param longitude
	 */
	public void setLongitude(Double longitude);

}
